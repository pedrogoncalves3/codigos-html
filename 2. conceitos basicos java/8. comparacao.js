// operadores logicos 
let nome1 = "marta"
let nome2 = "matheus"

let idade1 = 10
let idade2 = '11'

console.log('   ==Comparação==')
console.log('==Comparando conteudo==')
console.log(nome1 == nome2)


console.log('==Comparando tipo e conteudo==')
console.log(nome1 === nome2)


console.log('\n ==Diferente==')
console.log('==Comparando conteudo==')
console.log(idade1 != idade2)


console.log('==Comparando tipo e conteudo==')
console.log(idade1 !== idade2)


console.log('\n == and ==')
console.log(1 < 2 && 1<3)


console.log('\n == or ==')
console.log(10 < 2 || 1<3)




